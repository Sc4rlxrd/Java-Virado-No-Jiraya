package introducao;

public class Aula07Arrays01 {
    public static void main(String[] args) {
        // apenas tipo de referencia pode ser inicializada como null
        // exemplo arrays e Strings
        int [] idade = new int [3];
        idade[0] = 1;
        idade[1] = 11;
        idade[2] = 111;

        System.out.println(idade[0]);
        System.out.println(idade[1]);
        System.out.println(idade[2]);

    }
}
